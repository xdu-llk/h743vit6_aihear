#include <stdio.h>

/* Called from HardFault_Handler ASM with r0 = stacked frame pointer.
   Prints fault registers then loops forever. */
void hard_fault_printer(unsigned long *stacked) {
  (void)stacked;
  /* Use direct USART1 TX for crash output (printf may depend on interrupted state) */
  const char *msg = "\r\n[HARDFAULT]\r\n";
  for (const char *p = msg; *p; p++) {
    while (!(USART1->ISR & USART_ISR_TXFE)) {}
    USART1->TDR = *p;
  }
  char buf[64];
  int len = snprintf(buf, sizeof(buf), "PC=%08lX LR=%08lX CFSR=%08lX HFSR=%08lX\r\n",
                     stacked[6], stacked[5],
                     (*((volatile unsigned long *)0xE000ED28)),
                     (*((volatile unsigned long *)0xE000ED2C)));
  for (int i = 0; i < len; i++) {
    while (!(USART1->ISR & USART_ISR_TXFE)) {}
    USART1->TDR = buf[i];
  }
  __disable_irq();
  while (1) {}
}

